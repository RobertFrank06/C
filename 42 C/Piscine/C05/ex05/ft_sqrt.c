int	ft_sqrt(int nb)
{
	int	odd;
	int	count;

	if (nb < 0)
		return (0);
	odd = 1;
	count = 0;
	while (nb > 0)
	{
		nb -= odd;
		odd += 2;
		count++;
		if (nb == 0)
			return (count);
	}
	return (0);
}
